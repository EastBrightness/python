# 파이썬 -> python 으로 바꾸려면
# 변수 (수학) : x, y, z 같이 변하는 수
# 변수 (프로그래밍) : 데이터 저장 공간

공간1 = '파이썬' 
공간2 = 321

print(공간1)
print(공간2)

공간3 = 'hello 파이썬!'
공간2 = 111

print(공간2) 
print(공간3)

# = (수학) 같다, (프로그래밍) 대입
# = : 오른쪽에 있는 값을 왼쪽 공간에 담는다
공간1 = 'python'
# 공간1이라는 저장공간에 python을 담는다.

# 변수 활용 : 자주 바뀔 것 같거나 사용될 것 같은 값은 미리 저장해놓는다.
과목명 = '파이썬'
print('안녕 ' + 과목명)
